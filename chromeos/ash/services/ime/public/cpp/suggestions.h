#ifndef CHROMEOS_ASH_SERVICES_IME_PUBLIC_CPP_SUGGESTIONS_H_
#define CHROMEOS_ASH_SERVICES_IME_PUBLIC_CPP_SUGGESTIONS_H_

#include <string>
#include <vector>

namespace ash {
namespace ime {

enum class TextSuggestionMode {
  kPrediction = 0,
  kCompletion = 1,
};

enum class TextSuggestionType {
  kAssistivePersonalInfo = 0,
  kAssistiveEmoji = 1,
  kMultiWord = 2,
  kGrammar = 3,
  kLongpressDiacritic = 4,
};

// Encapsulates a single text based suggestion generated by the system.
struct TextSuggestion {
  // How should this suggestion be treated by a consumer?
  TextSuggestionMode mode;

  // What kind of suggestion is this?
  TextSuggestionType type;

  // The suggested text
  std::string text;

  // How many characters of the suggestion have already been typed by the user?
  size_t confirmed_length;

  bool operator==(const TextSuggestion& rhs) const {
    return (mode == rhs.mode && type == rhs.type && text == rhs.text &&
            confirmed_length == rhs.confirmed_length);
  }
};

struct TextCompletionCandidate {
  // Text to complete the current word
  std::string text;

  // The confidence scoring for the candidate
  float score;
};

// Specifies the current assistive window type being shown to the user.
enum class AssistiveWindowType {
  kNone,
  kUndoWindow,
  kEmojiSuggestion,
  kPersonalInfoSuggestion,
  kGrammarSuggestion,
  kMultiWordSuggestion,
  kLongpressDiacriticsSuggestion,
};

// Represents the current state of suggestions in the assistive window.
struct AssistiveWindow {
  AssistiveWindow();
  ~AssistiveWindow();

  AssistiveWindowType type;
  // Holds one or more candidates shown in the assistive window. When a single
  // candidate is shown in the window this list will contain exactly one
  // item, when multiple candidates are shown in the window it will contain
  // multiple items.
  std::vector<TextSuggestion> candidates;
};

}  // namespace ime
}  // namespace ash

#endif  // CHROMEOS_ASH_SERVICES_IME_PUBLIC_CPP_SUGGESTIONS_H_
