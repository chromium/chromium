#ifndef BASE_I18N_LANGUAGE_CODES_H_
#define BASE_I18N_LANGUAGE_CODES_H_

#include "base/i18n/language_code.h"
#include "base/i18n/language_code_builder.h"

// This file provides access to compile-time validated, canonical `LanguageCode`
// constants for all supported locales in Chromium.
//
// These constants should be used whenever you need to refer to a specific
// supported language code in a type-safe way, avoiding string literals.
//
// Usage:
//   const base::LanguageCode& en = base::language_codes::ENGLISH();
//   const base::LanguageCode& zh_cn = base::language_codes::CHINESE_CHINA();
//
// The functions return a reference to a static `LanguageCode` instance.
//
// The list of supported languages is generated by the action
// `canonical_language_codes_gen` and stored in
// `base/i18n/internal/canonical_language_codes.inc`.
namespace base::language_codes {

#define IMPL_LANGUAGECODE_TAG_NAME(tag, name) \
  BASE_I18N_EXPORT const base::LanguageCode& name();

#include "base/i18n/internal/canonical_language_codes.inc"
#undef IMPL_LANGUAGECODE_TAG_NAME

}  // namespace base::language_codes

#endif  // BASE_I18N_LANGUAGE_CODES_H_
