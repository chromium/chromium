#ifndef BASE_I18N_TAGS_H_
#define BASE_I18N_TAGS_H_

#include "base/i18n/language_tag.h"
#include "base/i18n/tag_converters.h"

// This file provides access to compile-time validated, canonical `LanguageTag`
// constants for all supported locales in Chromium.
//
// These constants should be used whenever you need to refer to a specific
// supported language tag in a type-safe way, avoiding string literals.
//
// Usage:
//   const base::LanguageTag& en = base::language_tags::ENGLISH();
//   const base::LanguageTag& zh_cn = base::language_tags::CHINESE_CHINA();
//
// The functions return a reference to a static `LanguageTag` instance.
//
// The list of supported languages is generated by the action
// `canonical_language_tags_gen` and stored in
// `base/i18n/internal/canonical_language_tags.inc`.
namespace base::language_tags {

#define IMPL_LANGUAGECODE_TAG_NAME(tag, name) \
  BASE_I18N_EXPORT const base::LanguageTag& name();

#include "base/i18n/internal/canonical_language_tags.inc"
#undef IMPL_LANGUAGECODE_TAG_NAME

}  // namespace base::language_tags

#endif  // BASE_I18N_TAGS_H_
