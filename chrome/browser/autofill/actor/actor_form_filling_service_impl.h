#include "components/autofill/core/browser/data_model/payments/credit_card.h"
namespace tabs {
class TabInterface;
}

namespace autofill {

// Implementation of the Actor -> Autofill interface, which is owned by
// `actor::ExecutionEngine`. Keeps the state generated by `GetSuggestions()`
// so that subsequent filling can refer to it by ID.
class ActorFormFillingServiceImpl : public ActorFormFillingService {
 public:
  ActorFormFillingServiceImpl();
  ActorFormFillingServiceImpl(const ActorFormFillingServiceImpl&) = delete;
  ActorFormFillingServiceImpl& operator=(const ActorFormFillingServiceImpl&) =
      delete;
  ActorFormFillingServiceImpl(ActorFormFillingServiceImpl&&) = delete;
  ActorFormFillingServiceImpl& operator=(ActorFormFillingServiceImpl&&) =
      delete;
  ~ActorFormFillingServiceImpl() override;

  // ActorFormFillingService:
  void GetSuggestions(
      const tabs::TabInterface& tab,
      base::span<const FillRequest> fill_requests,
      base::OnceCallback<
          void(base::expected<std::vector<ActorFormFillingRequest>,
                              ActorFormFillingError>)> callback) override;
  void FillSuggestions(
      const tabs::TabInterface& tab,
      base::span<const ActorFormFillingSelection> chosen_suggestions,
      base::OnceCallback<void(base::expected<void, ActorFormFillingError>)>
          callback) override;
  void ScrollToForm(const tabs::TabInterface& tab, int form_index) override;
  void PreviewForm(const tabs::TabInterface& tab,
                   int form_index,
                   ActorSuggestionId suggestion_id) override;
  void ClearFormPreview(const tabs::TabInterface& tab, int form_index) override;
  void FillForm(const tabs::TabInterface& tab,
                int form_index,
                ActorFormFillingSelection selection) override;

  // A record to keep track of the data that is backing an `ActorSuggestion`.
  struct FillData final {
    using Payload = std::variant<std::monostate, AutofillProfile, CreditCard>;

    FillData();
    FillData(std::vector<FieldGlobalId> field_ids, Payload filling_payload);
    FillData(const FillData&);
    FillData& operator=(const FillData&);
    FillData(FillData&&);
    FillData& operator=(FillData&&);
    ~FillData();

    // Returns whether the payload corresponds to a payments instrument.
    bool HasPaymentsPayload() const;

    // Fields that represents the form sections that are supposed to be filled.
    std::vector<FieldGlobalId> field_ids;
    Payload filling_payload;

    // Indicates if the form sections should be split, and if so what split this
    // FillData represents (either the contact info part or the address part).
    actor::SectionSplitPart split_part = actor::SectionSplitPart::kNoSplit;
  };

 private:
  friend class ActorFormFillingServiceImplTestApi;

  // Forwards a fill/preview signal to `AutofillManager`. Returns the error that
  // led to the filling failure if any, and `std::nullopt` otherwise.
  std::optional<ActorFormFillingError> FillOrPreviewFormImpl(
      const tabs::TabInterface& tab,
      ActorSuggestionId suggestion_id,
      mojom::ActionPersistence action_persistence);

  // Contains, within a single sequence of filling operations (all belonging to
  // a single round of suggestion generation), the errors (if any) that
  // prevented filling (not preview).
  std::vector<ActorFormFillingError> errors_per_session_;

  // A running counter used to generate `ActorSuggestionId`s.
  ActorSuggestionId::Generator suggestion_id_generator_;

  // A map from actor suggestions generated by `this` to the data required
  // for performing the form fill.
  base::flat_map<ActorSuggestionId, FillData> fill_data_;

  // Observes for the completion of the filling operations triggered by
  // `ActorFormFillingService::FillForm()`.
  std::unique_ptr<ActorFillingObserver> filling_observer_;

  // Contains the `FieldGlobalId` of the trigger field that was used to generate
  // suggestions during `ActorFormFillingServiceImpl::GetSuggestions()`.
  // The `i`th ID in that list corresponds to the `i`th generated
  // `FormFillingRequest`.
  // TODO(crbug.com/448398227): Consider dropping the ordering and instead
  // identify a `FormFillingRequest` by a `FormFillingRequestId`.
  std::vector<FieldGlobalId> suggestion_trigger_field_id_;

  base::WeakPtrFactory<ActorFormFillingServiceImpl> weak_ptr_factory_{this};
};

}  // namespace autofill

#endif  // CHROME_BROWSER_AUTOFILL_ACTOR_ACTOR_FORM_FILLING_SERVICE_IMPL_H_
