#ifndef CHROME_BROWSER_CHROMEOS_INPUT_METHOD_SUGGESTIONS_H_
#define CHROME_BROWSER_CHROMEOS_INPUT_METHOD_SUGGESTIONS_H_

#include <string>

#include "chrome/browser/chromeos/input_method/suggestion_enums.h"

namespace chromeos {

// Holds all relevant textual context required to generate suggestions.
struct SuggestionContext {
  // What kind of suggestions should be generated from this context?
  SuggestionMode mode;
};

// Encapsulates a single text based suggestion generated by the system.
struct TextSuggestion {
  // How should this suggestion be treated by a consumer?
  SuggestionMode mode;

  // What kind of suggestion is this?
  SuggestionType type;

  // The suggested text
  std::string text;

  bool operator==(const TextSuggestion& rhs) const {
    return (mode == rhs.mode && type == rhs.type && text == rhs.text);
  }
};

}  // namespace chromeos

#endif  // CHROME_BROWSER_CHROMEOS_INPUT_METHOD_SUGGESTIONS_H_
