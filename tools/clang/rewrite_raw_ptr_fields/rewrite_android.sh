set -e  # makes the script quit on any command failure

rm -fr ~/scratch
mkdir ~/scratch
BRANCH_PREFIX="rewrite20210312"

git checkout -b "${BRANCH_PREFIX}-android" --track origin/master
git cl patch 2649907
git cl issue 0
git add $0
git commit -m "Prepare ${BRANCH_PREFIX}"
git cl upload

gclient sync -D

cat > out/rewrite/args.gn <<- EOM
target_os = "android"
ffmpeg_branding = "Chrome"
is_chrome_branded = true
is_debug = false
is_official_build = true
proprietary_codecs = true
symbol_level = 1
use_goma = false
chrome_pgo_phase = 0
EOM
gn gen out/rewrite
time tools/clang/rewrite_raw_ptr_fields/rewrite.sh
git commit -am "rewrite generated by rewrite.sh"
cp -r ~/scratch ~/${BRANCH_PREFIX}-android-stats
git cl upload --bypass-hooks
