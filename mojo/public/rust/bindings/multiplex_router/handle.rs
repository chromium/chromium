//! This module defines the `MultiplexRouterHandle` type, which is held by
//! `Remote`s, and `Receiver`s. It serves as the public API for
//! `MultiplexRouter`s. A router handle is simply a reference to the
//! router, bundled together with that `Remote` or `Receiver`'s interface ID.

chromium::import! {
  "//mojo/public/rust/system";
  "//base:sequenced_task_runner";
}

use system::message_pipe::MessageEndpoint;

use crate::message::MojomMessage;

pub(crate) use super::router::EndpointInfo;
use super::router::{InterfaceId, MultiplexRouter};

/// A `MultiplexRouterHandle` is given out by a `MultiplexRouter` and can be
/// used to send messages to that router's underlying endpoint.
///
/// This object serves as the connection from remotes/receivers to the router.
/// It also notifies the router when it's dropped, signaling that it should run
/// the corresponding disconnect handler.
pub(crate) struct MultiplexRouterHandle {
    interface_id: InterfaceId,
    // This is a strong reference because the router may still need to process
    // tasks for associated interfaces even after the primary interface closes
    // (if there were messages that haven't yet been delivered). So we need to
    // make sure it remains open.
    router: MultiplexRouter,
}

impl Drop for MultiplexRouterHandle {
    fn drop(&mut self) {
        self.router.notify_dropped(self.interface_id)
    }
}

impl AsRef<MultiplexRouterHandle> for MultiplexRouterHandle {
    fn as_ref(&self) -> &Self {
        self
    }
}

impl PartialEq for MultiplexRouterHandle {
    fn eq(&self, other: &Self) -> bool {
        self.interface_id == other.interface_id && self.router.same_registry(&other.router)
    }
}

impl Eq for MultiplexRouterHandle {}

impl MultiplexRouterHandle {
    /// Create a new `MultiplexRouter` and return a handle to it. The returned
    /// handle will be bound to the router's primary interface.
    ///
    /// If `sets_high_bit` is true, then interface IDs generated by this router
    /// will have their high bit set to 1. This should be true for receivers,
    /// and false for remotes, to ensure both sides of the pipe don't generate
    /// colliding IDs.
    ///
    /// The router will invoke `incoming_message_handler` on each incoming
    /// message in FIFO order. Calls to the handler will be scheduled on the
    /// sequence specified in `endpoint_info`. If the other end of the pipe is
    /// closed and a disconnect handler is specified in `endpoint_info`, that
    /// handler will be scheduled on the sequence after all prior messages have
    /// been scheduled.
    pub fn new(
        endpoint: MessageEndpoint,
        sets_high_bit: bool,
        endpoint_info: EndpointInfo,
    ) -> Self {
        let router = MultiplexRouter::new(endpoint, sets_high_bit, endpoint_info);
        Self { interface_id: 0, router }
    }

    /// Create a new `MultiplexRouterHandle` from an interface ID and an
    /// existing router
    pub(super) fn from_parts(interface_id: InterfaceId, router: &MultiplexRouter) -> Self {
        Self { interface_id, router: router.clone_and_downgrade() }
    }

    /// Create a new `MultiplexRouterHandle` without an underlying endpoint
    pub fn new_for_testing(sets_high_bit: bool) -> Self {
        Self { interface_id: 0, router: MultiplexRouter::new_for_testing(sets_high_bit) }
    }

    /// Register a new associated endpoint (remote or receiver) with the
    /// underlying `MultiplexRouter`, and return a new handle with the
    /// appropriate ID.
    ///
    /// If the endpoint already has a known ID, then the router will use the
    /// provided one; otherwise, it will generate a new one. This function will
    /// panic if the provided ID already exists.
    ///
    /// If `endpoint_info` is `None`, then the endpoint will need to call `bind`
    /// later in order to provide it. Until that happens, the router will be
    /// unable to process any messages (or disconnect notifications) for this
    /// endpoint.
    pub fn register_new_endpoint(
        &self,
        interface_id: Option<InterfaceId>,
        endpoint_info: Option<EndpointInfo>,
    ) -> Option<Self> {
        let interface_id = self.router.add_associated_interface(interface_id, endpoint_info)?;
        Some(Self { interface_id, router: self.router.clone() })
    }

    /// Inform the `MultiplexRouter` that this endpoint has been bound to a
    /// sequence and is ready to start processing messages.
    pub fn bind(&self, endpoint_info: EndpointInfo) {
        self.router.bind_interface(self.interface_id, endpoint_info)
    }

    /// Send a message through the router with this handle's ID.
    pub fn send_message(&self, msg: MojomMessage) {
        self.router.send_message(msg, self.interface_id)
    }

    /// Return this handle's interface ID
    pub fn interface_id(&self) -> InterfaceId {
        self.interface_id
    }
}
