#ifndef COMPONENTS_MULTISTEP_FILTER_CORE_DATA_MODELS_FILTER_SUGGESTION_CANDIDATE_H_
#define COMPONENTS_MULTISTEP_FILTER_CORE_DATA_MODELS_FILTER_SUGGESTION_CANDIDATE_H_

#include <string>
#include <vector>

#include "base/uuid.h"
#include "url/gurl.h"

namespace multistep_filter {

// Represents an applied filter attribute for a
// `FilterSuggestionCandidate`.
struct FilterSuggestionCandidateAttribute {
  // The normalized key of the filter (e.g., "PRICE_MIN").
  std::string key;
  // User-visible label for the filter (e.g., "Minimum Price").
  std::u16string label;

  FilterSuggestionCandidateAttribute(std::string key, std::u16string label);

  FilterSuggestionCandidateAttribute(
      const FilterSuggestionCandidateAttribute&) = default;
  FilterSuggestionCandidateAttribute(FilterSuggestionCandidateAttribute&&) =
      default;
  FilterSuggestionCandidateAttribute& operator=(
      const FilterSuggestionCandidateAttribute&) = default;
  FilterSuggestionCandidateAttribute& operator=(
      FilterSuggestionCandidateAttribute&&) = default;

  ~FilterSuggestionCandidateAttribute() = default;

  std::string ToString() const;

  friend bool operator==(const FilterSuggestionCandidateAttribute&,
                         const FilterSuggestionCandidateAttribute&) = default;
};

// Represents a potential filter suggestion generated by the server.
// Contains some necessary data to display the suggestion in the UI (e.g.,
// `attributes` with user-friendly labels) and the action to take when
// applied (navigating to the `navigation_url`).
struct FilterSuggestionCandidate {
  // ID of the filter annotation which was used to generate this suggestion
  // candidate.
  base::Uuid filter_annotation_id;
  // The URL to navigate to when the suggestion is applied.
  GURL navigation_url;
  // Attributes applied to this suggestion.
  std::vector<FilterSuggestionCandidateAttribute> attributes;

  FilterSuggestionCandidate(
      base::Uuid filter_annotation_id,
      GURL navigation_url,
      std::vector<FilterSuggestionCandidateAttribute> attributes);

  FilterSuggestionCandidate(const FilterSuggestionCandidate&);
  FilterSuggestionCandidate(FilterSuggestionCandidate&&);
  FilterSuggestionCandidate& operator=(const FilterSuggestionCandidate&);
  FilterSuggestionCandidate& operator=(FilterSuggestionCandidate&&);

  ~FilterSuggestionCandidate();

  std::string ToString() const;

  friend bool operator==(const FilterSuggestionCandidate&,
                         const FilterSuggestionCandidate&);
};

}  // namespace multistep_filter

#endif  // COMPONENTS_MULTISTEP_FILTER_CORE_DATA_MODELS_FILTER_SUGGESTION_CANDIDATE_H_
