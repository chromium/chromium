#ifndef COMPONENTS_VISITED_URL_RANKING_PUBLIC_URL_GROUPING_SUGGESTIONS_DELEGATE_H_
#define COMPONENTS_VISITED_URL_RANKING_PUBLIC_URL_GROUPING_SUGGESTIONS_DELEGATE_H_

#include "base/functional/callback_forward.h"
#include "components/visited_url_ranking/public/url_grouping/group_suggestions.h"

namespace visited_url_ranking {

class SuggestionsDelegate {
 public:
  virtual ~SuggestionsDelegate() = 0;

  // The action taken by the user in response to the suggestion.
  enum class UserResponse {
    kUnknown = 0,
    kAccepted = 1,
    kRejected = 2,
    kIgnored = 3,
  };

  // The response of the user with metadata like edits made by the user or
  // feedback comments.
  struct UserResponseMetadata {
    UrlGroupingSuggestionId suggestion_id;
    UserResponse user_response;
  };

  using SuggestionResponseCallback =
      base::OnceCallback<const UserResponseMetadata>;

  // Shows suggestions generated by the service. Only called when the service
  // determines the suggestions are to be shown to the user. Returns true if
  // successfully shown.
  virtual bool ShowSuggestion(const GroupSuggestion& group_suggestions,
                              SuggestionResponseCallback response_callback) = 0;

  // Shows UI to share feedback with the team.
  virtual void OnDumpStateForFeedback(const std::string& dump_state) = 0;
};

}  // namespace visited_url_ranking

#endif  // COMPONENTS_VISITED_URL_RANKING_PUBLIC_URL_GROUPING_SUGGESTIONS_DELEGATE_H_
