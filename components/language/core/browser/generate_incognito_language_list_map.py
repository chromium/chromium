"""Generates the C++ header mapping language codes to incognito fallback sets.

Parses components_locale_settings.grd and corresponding translation .xtb files.
"""

import argparse
import os
import sys
import xml.etree.ElementTree as ET

# Add path to build/ to import action_helpers
sys.path.append(os.path.join(os.path.dirname(__file__), '../../../../build'))
import action_helpers

DEFAULT_EN_VALUE = "en-US,en"

def main():
    parser = argparse.ArgumentParser(
        description="Generate incognito language list map header.")
    parser.add_argument("grd_file", help="Path to the .grd file.")
    parser.add_argument("output_header", help="Path to the output header file.")
    parser.add_argument("--depfile", help="Path to the depfile.")
    args = parser.parse_args()

    grd_file = args.grd_file
    output_header = args.output_header
    depfile = args.depfile

    # Parse .grd to find .xtb files
    tree = ET.parse(grd_file)
    root = tree.getroot()

    xtb_files = []
    for translations in root.findall('.//translations'):
        for file_tag in translations.findall('file'):
            path = file_tag.get('path')
            lang = file_tag.get('lang')
            xtb_files.append((lang, path))

    map_data = {}
    for lang in DEFAULT_EN_VALUE.split(','):
        map_data[lang.strip()] = DEFAULT_EN_VALUE

    xtb_paths = []

    # Parse each .xtb file
    for lang, path in xtb_files:
        if lang in ('ar-XB', 'en-XA'):
            continue  # Skip pseudolocales

        full_path = os.path.join(os.path.dirname(grd_file), path)
        if not os.path.exists(full_path):
            print(f"Error: File not found {full_path}", file=sys.stderr)
            sys.exit(1)

        xtb_paths.append(full_path)

        xtb_tree = ET.parse(full_path)
        xtb_root = xtb_tree.getroot()

        for translation in xtb_root.findall('translation'):
            if translation.get('id') == 'IDS_ACCEPT_LANGUAGES':
                text = translation.text
                if text:
                    value = text.strip()
                    first_entry = value.split(',')[0].strip()

                    for k in [lang, first_entry]:
                        # Skip entries redundant with the not-found fallback
                        if k and value != f"{k},{DEFAULT_EN_VALUE}":
                            map_data[k] = value
                break



    # Generate C++ header
    os.makedirs(os.path.dirname(output_header), exist_ok=True)
    with action_helpers.atomic_output(output_header, mode='w') as f:
        f.write('// Generated by generate_incognito_language_list_map.py. '
                'DO NOT EDIT.\n\n')
        f.write('// Note: Entries that are redundant with the fallback logic'
                ' in\n')
        f.write('// language_prefs.cc (i.e. map to key + ",en-US,en") are'
                ' omitted.\n\n')
        f.write('#ifndef COMPONENTS_LANGUAGE_CORE_BROWSER_'
                'INCOGNITO_LANGUAGE_LIST_MAP_H_\n')
        f.write('#define COMPONENTS_LANGUAGE_CORE_BROWSER_'
                'INCOGNITO_LANGUAGE_LIST_MAP_H_\n\n')
        f.write('#include "base/containers/fixed_flat_map.h"\n')
        f.write('#include <string_view>\n\n')
        f.write('namespace language {\n\n')
        f.write('inline constexpr auto kIncognitoLanguageListMap = ')
        f.write('base::MakeFixedFlatMap<std::string_view,'
                ' std::string_view>({\n')

        for key in sorted(map_data.keys()):
            f.write(f'  {{"{key}", "{map_data[key]}"}},\n')

        f.write('});\n\n')
        f.write('}  // namespace language\n\n')
        f.write('#endif  // COMPONENTS_LANGUAGE_CORE_BROWSER_'
                'INCOGNITO_LANGUAGE_LIST_MAP_H_\n')

    # Write depfile if requested
    if depfile:
        action_helpers.write_depfile(depfile, output_header, xtb_paths)

if __name__ == '__main__':
    main()
